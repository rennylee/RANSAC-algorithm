public class Point3D {

    private double X;
    private double Y;
    private double Z;

    public Point3D(double x, double y, double z) {
        X=x;
        Y=y;
        Z=z;
    }

    public double getX(){return X;}

    public double getY(){return Y;}

    public double getZ(){return Z;}
}
